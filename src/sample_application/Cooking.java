package sample_application;

public class Cooking {

	public static void main(String[] args) {
		
		int step=0;
		
		switch(step) {
		case 0:
			System.out.println("문을 연다");
			System.out.println("냄비를 꺼낸다");
			System.out.println("씽그대로 냄비를 가져간다");
			break;
		case 1:		
			System.out.println("수도꼭지를 돌린다");
			System.out.println("냄비에 물을 채운다");
			System.out.println("물을 잠근다");
			break;
		case 2:
			System.out.println("가스 벨브를 연다");
			System.out.println("냄비를 옯긴다");
			System.out.println("불을 킨다");
		case 3:
			System.out.println("라면을 꺼낸다");
			System.out.println("봉지를 뜯는다");
			System.out.println("스프를 꺼낸다");
			System.out.println("스프를 뜯는다");
		case 4:
			System.out.println("냄비에 스프를 투척한다");
			System.out.println("물을 끓인다");
			System.out.println("라면을 인수분해한다");
			System.out.println("3분 대기한다");
		}
		
		
		




	}

}
